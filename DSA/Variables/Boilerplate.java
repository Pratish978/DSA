package Variables;

public class Boilerplate {
    public static void main(String[] args) {
        System.out.println();
    }
}
//BoilerplateCode

//Meaning of boilerplate:-
/* 
public: This is an access modifier.
        It means this class is visible and can be used by any other part of your program.

class: This keyword tells Java, "Hey, I am defining a new blueprint here."

Boilerplate: This is just the name of the class.
             It must exactly match the name of the file it's saved in (e.g., Boilerplate.java).

public: Again, this means Java can access this method from the outside to start the program.

static: This tells Java that it can run this method immediately without having to create a whole new object from the blueprint first.
        It saves memory and time at startup.

void: This means the method does its job and returns absolutely nothing back when it's done. 
      It just runs and stops.

main: This is the mandatory name of the method.

(String[] args): This allows your program to accept extra configuration or arguments from the command line when it starts up (like passing a file path or a username). 
                 Even if you don't use them, Java requires them to be there.

System: A built-in Java class that contains useful tools for interacting with the computer

.out: Short for "output".

.println: Short for "print line".
*/