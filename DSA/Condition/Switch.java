public class Switch {
    public static void main(String[] args) {
        int Number=3;
        switch (Number) {
            case 1:
                System.out.println("Burger");
                break;
            case 2:
                System.out.println("Fries");
                break;
            case 3:
                System.out.println("Coke");
            default:
                break;
        }
    }
}
